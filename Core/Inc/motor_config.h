/* === 文件名: motor_config.h === */
#ifndef __MOTOR_CONFIG_H
#define __MOTOR_CONFIG_H

/* ============================================================================ */
/* ======================== 1. 系统与硬件基础参数 ============================= */
/* ============================================================================ */
/* --- 核心基准频率 --- */
// 如果你在 CubeMX 里修改了时钟树，导致 TIM1 时钟改变，只需修改这里！
#define TIM1_CLOCK_FREQ     240000000.0f      // TIM1 定时器外设时钟 (Hz)

/* --- 频率与周期设置 --- */
#define PWM_FREQ            40000.0f          // PWM 开关频率 (Hz)
#define TS                  (1.0f/PWM_FREQ)   // 离散系统采样周期 (s)

/* --- 定时器与 PWM 限幅 --- */
// 自动推导 ARR: ARR = TIM_CLK / (2 * PWM_FREQ)
#define TIM1_ARR_VALUE      ((uint16_t)(TIM1_CLOCK_FREQ / (2.0f * PWM_FREQ))) 

// 自动推导周期计数值: 等于 ARR 的两倍 (用于占空比归一化和除法运算)
#define TIM1_PERIOD         ((uint16_t)(TIM1_ARR_VALUE * 2)) 

/* --- 占空比安全限幅推导 --- */
// 原来你设定的是 2800/3000 ≈ 93.33%，我们提取出这个安全比例
#define MAX_DUTY_RATIO      0.933333f         // 预留约 6.6% 的时间给自举电容充电
// 自动推导限幅计数值
#define PWM_DUTY_LIMIT      ((uint16_t)(TIM1_ARR_VALUE * MAX_DUTY_RATIO))

/* --- 死区时间 --- */
#define DEADTIME_REG_VAL    120               // 死区时间寄存器值 (防止上下桥臂直通)


/* ============================================================================ */
/* ======================== 2. ADC 采样换算系数 =============================== */
/* ============================================================================ */

#define ADC_VOLTAGE_COEFF   (441.3f / 4096.0f * 1.212f) // 母线电压换算系数 (V/LSB)
#define ADC_CURRENT_COEFF   (50.0f / 4096.0f)           // 相电流换算系数 (A/LSB)
#define UDC_NOMINAL         310.0f                      // 系统额定母线电压 (V)


/* ============================================================================ */
/* ======================== 3. 电机本体参数 =================================== */
/* ============================================================================ */

#define POLE_PAIRS          3               // 电机极对数
#define MOTOR_LD            0.00012f        // 直轴电感 (H)
#define MOTOR_LQ            0.00025f        // 交轴电感 (H)
#define MOTOR_FLUX          0.015f          // 永磁体磁链 (Wb)
#define ENCODER_LINES       4096            // 增量式编码器单圈线数/分辨率


/* ============================================================================ */
/* ======================== 4. FOC 控制环路参数 (PI) ========================== */
/* ============================================================================ */

#define USE_ANTI_WINDUP  1  // 1:使用抗饱和PI, 0:使用普通盲目积分PI

/* D轴电流环 (控制励磁/弱磁) */
#define KP_ID               25.2f             // D轴电流环比例增益
#define KI_ID               0.22f             // D轴电流环积分增益

/* Q轴电流环 (控制转矩) */
#define KP_IQ               35.4f             // Q轴电流环比例增益
#define KI_IQ               0.22f             // Q轴电流环积分增益

/* 速度外环 */
#define KP_SPD              0.3f              // 速度环比例增益
#define KI_SPD              0.00002f          // 速度环积分增益

/* 无感观测器锁相环 (PLL) */
#define KP_PLL              444.3f            // 角度追踪比例增益
#define KI_PLL              2.468f            // 角度追踪积分增益

/* ============================================================================ */
/* ======================== 7. 算法策略与模式选择 (Strategy & Mode) =========== */
/* ============================================================================ */

/* --- 控制策略选择 --- */
#define CTRL_STRATEGY_VF        0   // 开环 V/F 控制 (强拖启动/测试)
#define CTRL_STRATEGY_FOC_ID0   1   // 闭环 FOC (Id=0 控制，表贴式SPM标配)
#define CTRL_STRATEGY_FOC_MTPA  2   // 闭环 FOC (最大转矩电流比，内置式IPM标配)

#define CURRENT_CTRL_STRATEGY   CTRL_STRATEGY_FOC_ID0 //  在此切换当前控制策略

/* --- 无感观测器选择 --- */
#define SENSORLESS_NONE         0   // 纯有感控制 (完全依赖编码器)
#define SENSORLESS_HFI          1   // 高频注入法 (零/低速域)
#define SENSORLESS_SMO          2   // 滑模观测器 (中/高速域)
#define SENSORLESS_HYBRID       3   // HFI + SMO 平滑切换 (全速域无感)

#define CURRENT_SENSORLESS_MODE SENSORLESS_HFI        // 在此切换当前无感模式


/* ============================================================================ */
/* ======================== 5. 高频注入法 (HFI) 参数 ========================== */
/* ============================================================================ */

/* 注入模式定义 */
#define HFI_MODE_SQUARE     0                 // 方波注入模式
#define HFI_MODE_SINUSOIDAL 1                 // 正弦波注入模式

/* 当前使用的注入策略 */
#define CURRENT_HFI_MODE    HFI_MODE_SINUSOIDAL 

#define HFI_AMP             20.0f             // 注入电压幅值
#define HFI_FREQ            2500.0f           // 注入频率 (Hz)


/* ============================================================================ */
/* ======================== 6. 数字滤波器参数 ================================= */
/* ============================================================================ */

#define SPEED_LPF_FC        20.0f             // 速度观测低通截止频率 (Hz)
#define IDQ_HPF_FC          400.0f            // HFI 提取基波用高通截止频率 (Hz)
#define IDQ_LPF_FC          500.0f            // HFI 提取基波用低通截止频率 (Hz)

#endif /* __MOTOR_CONFIG_H */
